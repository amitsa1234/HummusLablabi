var maxAttempts = 3;
var successfulLogin = false;

for (var attempts = 1; attempts <= maxAttempts; attempts++) {
  var username = prompt('נא הזן את שם המשתמש:');
  var password = prompt('נא הזן את הקוד:');

  if (username === 'admin' && password === '12345') {
    successfulLogin = true;
    alert('פרטי הכניסה נכונים! מיד תועבר.');
    break;
  } else {
    alert('שם משתמש או קוד שגויים! נסה שוב.');
  }
}

if (!successfulLogin) {
  window.location.href = 'enter.html';
}
